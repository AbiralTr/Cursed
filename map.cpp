Map::Map(){
    dirt = TextureManager::LoadTexture("assets/tiles/dirt.png");
    grass = TextureManager::LoadTexture("assets/tiles/grass.png");
    water = TextureManager::LoadTexture("assets/tiles/water.png");

    loadMap(demo_map);

    src.x = 0;
    src.y = 0;
    dest.x = 0;
    dest.y = 0;
    src.w = 32;
    src.h = 32;
    dest.w = 32;
    dest.h = 32;
    
    
}

void Map::loadMap(int arr[20][25]){
    for(int i = 0; i < 20; i++){
        for(int j = 0; j < 25; j++){
            map[i][j] = arr[i][j];
        }
    }
}

void Map::drawMap(){
    int type = 0;
    for(int i = 0; i < 20; i++){
        for(int j = 0; j < 25; j++){
            type = map[i][j];
            
            dest.x = j * 32;
            dest.y = i * 32;

            switch(type){
                case 0:
                    TextureManager::Draw(water, src, dest);
                    break;
                case 1:
                    TextureManager::Draw(grass, src, dest);
                    break;
                case 2:
                    TextureManager::Draw(dirt, src, dest);
                    break;
                default:

                    break;
            }
        }
    }
}